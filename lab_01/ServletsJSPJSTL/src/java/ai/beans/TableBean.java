package ai.beans;

public class TableBean {
    
    private boolean tableBoarding;

    public TableBean() {
    }
    
    /**
     * @return the tableBoarding
     */
    public boolean isTableBoarding() {
        return tableBoarding;
    }

    /**
     * @param tableBoarding the tableBoarding to set
     */
    public void setTableBoarding(boolean tableBoarding) {
        this.tableBoarding = tableBoarding;
    }
    
}
